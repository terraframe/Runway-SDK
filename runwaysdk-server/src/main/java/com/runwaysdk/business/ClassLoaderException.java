package com.runwaysdk.business;

import com.runwaysdk.ServerExceptionMessageLocalizer;
import com.runwaysdk.dataaccess.MetadataDAOIF;


/**
 * Indicates an error when dynamically loading a generated class. Most often
 * used as a wrapper for exceptions thrown by the reflection API.
 * 
 * @author Eric Grunzke
 */
public class ClassLoaderException extends BusinessException
{
  /**
   * Generated By Eclipse
   */
  private static final long serialVersionUID = 657513644781118102L;
  
  /**
   * Metadata describing the type that was loaded
   */
  private MetadataDAOIF metadata;
  
  /**
   * Constructs a new ClassLoaderException with the specified developer message.
   * 
   * @param devMessage
   *          The non-localized developer error message. Contains specific data
   *          access layer information useful for application debugging. The
   *          developer message is saved for later retrieval by the
   *          {@link #getMessage()} method.
   * @param metadata
   *          Metadata describing the type that was loaded
   */
  public ClassLoaderException(String devMessage, MetadataDAOIF metadata)
  {
    super(devMessage);
    this.metadata = metadata;
  }

  /**
   * Constructs a new ClassLoaderException with the specified developer message and
   * cause.
   * <p>
   * Note that the detail message associated with <code>cause</code> is <i>not</i>
   * automatically incorporated in this ClassLoaderException's detail message.
   * 
   * @param devMessage
   *          The non-localized developer error message. Contains specific data
   *          access layer information useful for application debugging. The
   *          developer message is saved for later retrieval by the
   *          {@link #getMessage()} method.
   * @param metadata
   *          Metadata describing the type that was loaded
   * @param cause
   *          the cause (which is saved for later retrieval by the
   *          {@link #getCause()} method). (A <tt>null</tt> value is
   *          permitted, and indicates that the cause is nonexistent or
   *          unknown.)
   */
  public ClassLoaderException(String devMessage, MetadataDAOIF metadata, Throwable cause)
  {
    super(devMessage, cause);
    this.metadata = metadata;
  }

  /**
   * Constructs a new ClassLoaderException with the specified cause and a developer
   * message taken from the cause. This constructor is useful if the
   * ClassLoaderException is a wrapper for another throwable.
   * 
   * @param metadata
   *          Metadata describing the type that was loaded
   * @param cause
   *          the cause (which is saved for later retrieval by the
   *          {@link #getCause()} method). (A <tt>null</tt> value is
   *          permitted, and indicates that the cause is nonexistent or
   *          unknown.)
   */
  public ClassLoaderException(MetadataDAOIF metadata, Throwable cause)
  {
    super(cause);
    this.metadata = metadata;
  }

  /**
   * Fetches the localized message template and plugs in the correct parameters
   * to set the business error message.
   * 
   */
  public String getLocalizedMessage()
  {
    return ServerExceptionMessageLocalizer.classLoaderException(this.getLocale(), metadata);
  }
}
