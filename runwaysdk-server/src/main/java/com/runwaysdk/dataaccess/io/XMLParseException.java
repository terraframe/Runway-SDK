package com.runwaysdk.dataaccess.io;

import org.xml.sax.Locator;
import org.xml.sax.SAXParseException;

/**
 * Represents the special case where an XML document cannor be parsed correctly. This can
 * be either a syntactic error, or a logical error in the document.
 * 
 * @author Eric Grunzke, Richard Rowlands
 */
public class XMLParseException extends XMLException
{
  /**
   * Generated by Eclipse
   */
  private static final long serialVersionUID = 6755049289671008236L;

  /**
   * Constructs a new XMLParseException with the specified developer message.
   * 
   * @param devMessage
   *          The non-localized developer error message. Contains specific data access
   *          layer information useful for application debugging. The developer message is
   *          saved for later retrieval by the {@link #getMessage()} method.
   */
  public XMLParseException(String devMessage)
  {
    super(devMessage);
  }

  /**
   * Constructs a new XMLParseException with the specified developer message and cause.
   * <p>
   * Note that the detail message associated with <code>cause</code> is <i>not</i>
   * automatically incorporated in this XMLParseException's detail message.
   * 
   * @param devMessage
   *          The non-localized developer error message. Contains specific data access
   *          layer information useful for application debugging. The developer message is
   *          saved for later retrieval by the {@link #getMessage()} method.
   * @param cause
   *          the cause (which is saved for later retrieval by the {@link #getCause()}
   *          method). (A <tt>null</tt> value is permitted, and indicates that the cause
   *          is nonexistent or unknown.)
   */
  public XMLParseException(String devMessage, Throwable cause)
  {
    super(devMessage, cause);
  }

  /**
   * Constructs a new XMLParseException with the specified cause and a developer message
   * taken from the cause. This constructor is useful if the XMLParseException is a
   * wrapper for another throwable.
   * 
   * @param cause
   *          the cause (which is saved for later retrieval by the {@link #getCause()}
   *          method). (A <tt>null</tt> value is permitted, and indicates that the cause
   *          is nonexistent or unknown.)
   */
  public XMLParseException(Throwable cause)
  {
    super(cause);
  }
  
  public XMLParseException(Locator locator, Throwable e) {
    super("An error occurred while parsing " + (locator.getSystemId() == null ? "xml" : locator.getSystemId()) + " on line " + locator.getLineNumber() + ":" + locator.getColumnNumber() + ". " + e.getLocalizedMessage(), e);
  }
  
  public XMLParseException(SAXParseException saxEx)
  {
    super("An error occurred while parsing " + (saxEx.getSystemId() == null ? "xml" : saxEx.getSystemId()) + " on line " + saxEx.getLineNumber() + ":" + saxEx.getColumnNumber() + ". " + saxEx.getLocalizedMessage(), saxEx);
  }
  
  public XMLParseException(StreamSource source, SAXParseException saxEx)
  {
    super("An error occurred while parsing " + source.getToString() + " on line " + saxEx.getLineNumber() + ":" + saxEx.getColumnNumber() + ". " + saxEx.getLocalizedMessage(), saxEx);
  }
}
