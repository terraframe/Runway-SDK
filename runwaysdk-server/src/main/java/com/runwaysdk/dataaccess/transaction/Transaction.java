package com.runwaysdk.dataaccess.transaction;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

/**
 * This annotation is used as a hook to weave in database transaction management.  Any
 * method outside the data access layer that is a transaction should have this annotation.
 * @author nathan
 *
 */
@Retention(RetentionPolicy.RUNTIME)
@Target(ElementType.METHOD)
public @interface Transaction
{
  public TransactionType value() default TransactionType.DEFAULT;
}
