package com.runwaysdk.dataaccess.attributes;

import com.runwaysdk.ServerExceptionMessageLocalizer;
import com.runwaysdk.dataaccess.AttributeIF;


/**
 * Thrown when the value set for an attribute is invalid
 * 
 * @author Eric Grunzke
 */
public class AttributeValueException extends AttributeException
{
  /**
   * Generated by Eclipse
   */
  private static final long serialVersionUID = -6056044266797826047L;

  /**
   * The attribute being set
   */
  protected AttributeIF       attribute;

  /**
   * Constructs a new AttributeValueException with the specified developer message. This constructor
   * uses a different localized message since no Attribute is specified. Use this constructor for when
   * there is no Attribute available.
   * 
   * @param devMessage
   *          The non-localized developer error message. Contains specific data access
   *          layer information useful for application debugging. The developer message is
   *          saved for later retrieval by the {@link #getMessage()} method.
   * @param value
   *          The invalid value
   */
  public AttributeValueException(String devMessage, String value)
  {
    super(devMessage);
    this.value = value;
    this.attribute = null;
  }
  
  /**
   * The value that isn't valid
   */
  protected String            value;

  /**
   * Constructs a new AttributeValueException with the specified developer message.
   * 
   * @param devMessage
   *          The non-localized developer error message. Contains specific data access
   *          layer information useful for application debugging. The developer message is
   *          saved for later retrieval by the {@link #getMessage()} method.
   * @param attribute
   *          The attribute being set
   * @param value
   *          The invalid value
   */
  public AttributeValueException(String devMessage, AttributeIF attribute, String value)
  {
    super(devMessage);
    this.attribute = attribute;
    this.value = value;
  }

  /**
   * Constructs a new AttributeValueException with the specified developer message and
   * cause.
   * <p>
   * Note that the detail message associated with <code>cause</code> is <i>not</i>
   * automatically incorporated in this AttributeValueException's detail message.
   * 
   * @param devMessage
   *          The non-localized developer error message. Contains specific data access
   *          layer information useful for application debugging. The developer message is
   *          saved for later retrieval by the {@link #getMessage()} method.
   * @param cause
   *          the cause (which is saved for later retrieval by the {@link #getCause()}
   *          method). (A <tt>null</tt> value is permitted, and indicates that the cause
   *          is nonexistent or unknown.)
   * @param attribute
   *          The attribute being set
   * @param value
   *          The invalid value
   */
  public AttributeValueException(String devMessage, Throwable cause, AttributeIF attribute, String value)
  {
    super(devMessage, cause);
    this.attribute = attribute;
    this.value = value;
  }

  /**
   * Constructs a new AttributeValueException with the specified cause and a developer
   * message taken from the cause. This constructor is useful if the
   * AttributeValueException is a wrapper for another throwable.
   * 
   * @param cause
   *          the cause (which is saved for later retrieval by the {@link #getCause()}
   *          method). (A <tt>null</tt> value is permitted, and indicates that the cause
   *          is nonexistent or unknown.)
   * @param attribute
   *          The attribute being set
   * @param value
   *          The invalid value
   */
  public AttributeValueException(Throwable cause, AttributeIF attribute, String value)
  {
    super(cause);
    this.attribute = attribute;
    this.value = value;
  }

  /**
   * Fetches the localized message template and plugs in the correct parameters to set the
   * business error message.
   * 
   */
  public String getLocalizedMessage()
  {
    if(attribute != null)
      return ServerExceptionMessageLocalizer.attributeValueException(this.getLocale(), this.attribute, this.value);
    else
      return ServerExceptionMessageLocalizer.attributeValueExceptionGeneric(this.getLocale(), this.value);
  }
}
