package com.runwaysdk.dataaccess.metadata;

import com.runwaysdk.ServerExceptionMessageLocalizer;


/**
 * Thrown when an entity or attribtue name is reserved
 * 
 * @author Eric Grunzke
 */
public class ReservedWordException extends MetadataException
{
  /**
   * Generated by Eclipse
   */
  private static final long serialVersionUID = 8926136217960889768L;

  /**
   * The reserved word
   */
  private String reservedWord;
  
  /**
   * Where the exception originated from (table, attribute, etc), used to give
   * more helpful info to the end user.
   */
  private ReservedWordException.Origin exceptionOrigin;
  
  /**
   * Constructor
   * Same as others except this one takes an origin
   */
  public ReservedWordException(String devMessage, String reservedWord, ReservedWordException.Origin origin)
  {
    super(devMessage);
    this.reservedWord = reservedWord;
    this.exceptionOrigin = origin;
  }
  
  /**
   * Constructs a new ReservedWordException with the specified developer message
   * and a default business message. Leaving the default business message is
   * discouraged, as it provides no context information for end users.
   * 
   * @param devMessage
   *          The non-localized developer error message. Contains specific data
   *          access layer information useful for application debugging. The
   *          developer message is saved for later retrieval by the
   *          {@link #getMessage()} method.
   * @param reservedWord
   *          The reserved word
   */
  public ReservedWordException(String devMessage, String reservedWord)
  {
    this(devMessage, reservedWord, Origin.NONE);
  }

  /**
   * Constructs a new ReservedWordException with the specified detail message
   * and cause.
   * <p>
   * Note that the detail message associated with <code>cause</code> is <i>not</i>
   * automatically incorporated in this ReservedWordException's detail message.
   * 
   * @param devMessage
   *          The non-localized developer error message. Contains specific data
   *          access layer information useful for application debugging. The
   *          developer message is saved for later retrieval by the
   *          {@link #getMessage()} method.
   * @param cause
   *          the cause (which is saved for later retrieval by the
   *          {@link #getCause()} method). (A <tt>null</tt> value is
   *          permitted, and indicates that the cause is nonexistent or
   *          unknown.)
   * @param reservedWord
   *          The reserved word
   */
  public ReservedWordException(String devMessage, Throwable cause, String reservedWord)
  {
    super(devMessage, cause);
    this.reservedWord = reservedWord;
    this.exceptionOrigin = Origin.NONE;
  }

  /**
   * Constructs a new ReservedWordException with the specified cause and a
   * developer message taken from the cause. This constructor is useful if the
   * ReservedWordException is a wrapper for another throwable.
   * 
   * @param cause
   *          the cause (which is saved for later retrieval by the
   *          {@link #getCause()} method). (A <tt>null</tt> value is
   *          permitted, and indicates that the cause is nonexistent or
   *          unknown.)
   * @param reservedWord
   *          The reserved word
   */
  public ReservedWordException(Throwable cause, String reservedWord)
  {
    super(cause);
    this.reservedWord = reservedWord;
    this.exceptionOrigin = Origin.NONE;
  }
  
  /**
   * Uses the stored parameters and the given Locale to produce an error message
   * suitable for display to end users.
   *
   */
  public String getLocalizedMessage()
  {
    return ServerExceptionMessageLocalizer.reservedWordException(this.getLocale(), this.reservedWord, this.exceptionOrigin);
  }
  
  public ReservedWordException.Origin getOrigin()
  {
    return this.exceptionOrigin;
  }
  
  public enum Origin {
    
    // These strings are added to "ReservedWordException" in ServerExceptionMessageLocalizer
    // to form a string that matches the various ReservedWordException's in serverExceptions.properties
    // in order to give more information about where this exception originated to the end user.
    
    NONE              (""),
    
    TYPE              ("Type"),
    
    TABLE             ("Table"),
    
    ATTRIBUTE         ("Attribute"),
    
    COLUMN            ("Column"),
    
    TRANSITION        ("Transition"),
    
    STATE             ("State");
    
    private String key;
    
    private Origin(String str)
    {
      this.key = str;
    }
    
    public String getKey()
    {
      return this.key;
    }
  }
}
