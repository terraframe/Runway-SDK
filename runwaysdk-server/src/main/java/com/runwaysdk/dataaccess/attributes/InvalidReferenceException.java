package com.runwaysdk.dataaccess.attributes;

import com.runwaysdk.ServerExceptionMessageLocalizer;
import com.runwaysdk.dataaccess.MdAttributeRefDAOIF;
import com.runwaysdk.dataaccess.MdAttributeReferenceDAOIF;


/**
 * Thrown when an attribute references an invalid instance. This can arise from
 * an incorrect oid, or an incompatible type.
 * 
 * @author Eric Grunzke
 */
public class InvalidReferenceException extends AttributeException
{
  /**
   * Generated by Eclipse
   */
  private static final long serialVersionUID = 3952724326019068259L;
  
  /**
   * Metadata describing the attribute with the invalid reference
   */
  private MdAttributeRefDAOIF mdAttributeReference;
  
  /**
   * Constructs a new InvalidReferenceException with the specified developer
   * message.
   * 
   * @param devMessage
   *          The non-localized developer error message. Contains specific data
   *          access layer information useful for application debugging. The
   *          developer message is saved for later retrieval by the
   *          {@link #getMessage()} method.
   * @param mdAttributeReference
   *          MdAttributeReference decribing the attribute with the bad value
   */
  public InvalidReferenceException(String devMessage, MdAttributeRefDAOIF mdAttributeReference)
  {
    super(devMessage);
    this.mdAttributeReference = mdAttributeReference;
  }

  /**
   * Constructs a new InvalidReferenceException with the specified developer message and
   * cause.
   * <p>
   * Note that the detail message associated with <code>cause</code> is <i>not</i>
   * automatically incorporated in this InvalidReferenceException's detail message.
   * 
   * @param devMessage
   *          The non-localized developer error message. Contains specific data
   *          access layer information useful for application debugging. The
   *          developer message is saved for later retrieval by the
   *          {@link #getMessage()} method.
   * @param cause
   *          the cause (which is saved for later retrieval by the
   *          {@link #getCause()} method). (A <tt>null</tt> value is
   *          permitted, and indicates that the cause is nonexistent or
   *          unknown.)
   * @param mdReference
   *          MdAttributeReference decribing the attribute with the bad value
   */
  public InvalidReferenceException(String devMessage, Throwable cause, MdAttributeReferenceDAOIF mdAttributeReference)
  {
    super(devMessage, cause);
    this.mdAttributeReference = mdAttributeReference;
  }

  /**
   * Constructs a new InvalidReferenceException with the specified cause and a developer
   * message taken from the cause. This constructor is useful if the
   * InvalidReferenceException is a wrapper for another throwable.
   * 
   * @param cause
   *          the cause (which is saved for later retrieval by the
   *          {@link #getCause()} method). (A <tt>null</tt> value is
   *          permitted, and indicates that the cause is nonexistent or
   *          unknown.)
   * @param mdAttributeReference
   *          MdAttributeReference decribing the attribute with the bad value
   */
  public InvalidReferenceException(Throwable cause, MdAttributeReferenceDAOIF mdAttributeReference)
  {
    super(cause);
    this.mdAttributeReference = mdAttributeReference;
  }

  /**
   * Fetches the localized message template and plugs in the correct parameters
   * to set the business error message.
   * 
   */
  public String getLocalizedMessage()
  {
    return ServerExceptionMessageLocalizer.invalidReferenceException(this.getLocale(), this.mdAttributeReference, this.mdAttributeReference.getReferenceMdBusinessDAO());
  }
  
  public MdAttributeRefDAOIF getMdAttributeReference()
  {
    return this.mdAttributeReference;
  }
}
