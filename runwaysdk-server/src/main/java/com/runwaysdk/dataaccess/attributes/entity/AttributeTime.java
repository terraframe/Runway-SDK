package com.runwaysdk.dataaccess.attributes.entity;

import java.util.Date;
import java.util.regex.Pattern;

import com.runwaysdk.constants.MdAttributeTimeUtil;
import com.runwaysdk.dataaccess.MdAttributeDAOIF;
import com.runwaysdk.dataaccess.attributes.AttributeValueException;
import com.runwaysdk.dataaccess.database.Database;

public class AttributeTime extends AttributeMoment
{
  /**
   * Generated by Eclipse
   */
  private static final long    serialVersionUID = -1723836642633855026L;

  /**
   * Regex pattern to match times.
   */
  private static final Pattern timePattern      = Pattern.compile("(\\d){2}:[0-5][0-9]:[0-5][0-9]");

  /**
   * Inherited constrcutor, sets <code>name</code> and
   * <code>definingEntityType</code>.
   *
   * @param name
   *          The name of this time attribute.
   * @param mdAttributeKey
   *          key of the defining attribute metadata.
   * @param definingEntityType
   *          Name of the entity that defines this attribute.
   */
  protected AttributeTime(String name, String mdAttributeKey, String definingEntityType)
  {
    super(name, mdAttributeKey, definingEntityType);
  }

  /**
   * Inherited constrcutor, sets <code>name</code>,
   * <code>definingEntityType</code>, and <code>value</code>.
   *
   * @param name
   *          The name of this time attribute.
   * @param mdAttributeKey
   *          key of the defining attribute metadata.
   * @param definingEntityType
   *          Name of the entity that defines this attribute.
   * @param value
   *          The value of this time. "<code>true</code>" or
   *          "<code>false</code>"
   */
  protected AttributeTime(String name, String mdAttributeKey, String definingEntityType, String value)
  {
    super(name, mdAttributeKey, definingEntityType, value);

    this.value = removeMillisecondDelimiter(this.value);

    this.value = formatSQLToJavaTime(this.value);
  }

  @Override
  public void setValue(Object value)
  {
    if (value != null && value instanceof Date)
    {
      this.setValue(MdAttributeTimeUtil.getTypeUnsafeValue((Date) value));
    }
    else
    {
      super.setValue(value);
    }
  }

  @Override
  public Object getObjectValue()
  {
    if (this.getValue() != null && this.getValue().length() > 0)
    {
      return MdAttributeTimeUtil.getTypeSafeValue(this.getValue());
    }

    return super.getObjectValue();
  }

  /**
   *
   * @param value
   * @return
   */
  public static String removeMillisecondDelimiter(String value)
  {
    String returnValue = value;

    // check for "." which is the delimiter for milliseconds
    if (returnValue.contains("."))
    {
      // set this.value as substring cutting off milliseconds
      returnValue = returnValue.substring(0, value.indexOf("."));
    }

    return returnValue;
  }

  /**
   *
   * @param value
   * @return
   */
  public static String formatSQLToJavaTime(String value)
  {
    String returnValue = value;

    // Some databases (like Oracle) will store the full date and time.
    // Consequently, the
    // database will return the full date and time. We need to strip off the
    // date and
    // only store the time.
    if (returnValue != null && !returnValue.trim().equals("") && returnValue.contains("-"))
    {
      returnValue = Database.formatSQLToJavaTime(returnValue);
    }

    return returnValue;
  }

  /**
   * Test if the input String is valid.
   *
   * <br/>
   * <b>Precondition: </b> valueToValidate != null <br/>
   * <b>Postcondition: </b> true
   *
   * @param valueToValidate
   *          the primitive attribute value to be validated
   * @param mdAttribute
   *          the defining Metadata object of the class that contains this
   *          Attribute
   */
  protected void validate(String valueToValidate, MdAttributeDAOIF mdAttribute)
  {
    super.validate(valueToValidate, mdAttribute);
    if (!isValid(valueToValidate))
    {
      String error = "Value [" + valueToValidate + "] is invalid for attribute [" + getName() + "] on type [" + getDefiningClassType() + "]";
      throw new AttributeValueException(error, this, valueToValidate);
    }
  }

  /**
   * Test if the input String is valid.
   *
   * <br/>
   * <b>Precondition: </b> valueToValidate != null <br/>
   * <b>Postcondition: </b> true
   *
   * @param value
   *          the primitive attribute value to be validated
   * @return <code><b>true</b></code> if the value is a valid Date
   */
  public static boolean isValid(String value)
  {
    // check against the regex pattern
    if (!value.trim().equals("") && ( !timePattern.matcher(value).matches() || !AttributeMoment.checkTimeBounds(value) ))
    {
      return false;
    }
    return true;
  }

  /**
   * Returns a deep clone of this attribute.
   *
   * <br/>
   * <b>Precondition: </b> true <br/>
   * <b>Postcondition: </b> true
   *
   * @return a deep clone of this Attribute
   */
  public AttributeTime attributeClone()
  {
    return new AttributeTime(this.getName(), this.mdAttributeKey, this.getDefiningClassType(), new String(this.getRawValue()));
  }
}
